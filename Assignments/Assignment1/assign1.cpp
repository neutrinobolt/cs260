#include <iostream>

int main() {

    // declare starting marble number

    int marbles = 7;
    printf("Marbles: %i\n", marbles); // Should show 7

    // add some marbles
    marbles = marbles + 4;
    printf("Marbles: %i\n", marbles); // Should show 11

    // subtract marbles
    marbles = marbles - 5;
    printf("Marbles: %i\n", marbles); // Should show 6

    return 0;
}